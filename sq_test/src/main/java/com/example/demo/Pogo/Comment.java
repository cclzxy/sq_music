package com.example.demo.Pogo;


public class Comment {

  private long commentid;
  private String comment;
  private String comtime;
  private double comscore;
  private String coma;
  private String comb;
  private String comc;
  private String comd;
  private String come;


  public long getCommentid() {
    return commentid;
  }

  public void setCommentid(long commentid) {
    this.commentid = commentid;
  }


  public String getComment() {
    return comment;
  }

  public void setComment(String comment) {
    this.comment = comment;
  }


  public String getComtime() {
    return comtime;
  }

  public void setComtime(String comtime) {
    this.comtime = comtime;
  }


  public double getComscore() {
    return comscore;
  }

  public void setComscore(double comscore) {
    this.comscore = comscore;
  }


  public String getComa() {
    return coma;
  }

  public void setComa(String coma) {
    this.coma = coma;
  }


  public String getComb() {
    return comb;
  }

  public void setComb(String comb) {
    this.comb = comb;
  }


  public String getComc() {
    return comc;
  }

  public void setComc(String comc) {
    this.comc = comc;
  }


  public String getComd() {
    return comd;
  }

  public void setComd(String comd) {
    this.comd = comd;
  }


  public String getCome() {
    return come;
  }

  public void setCome(String come) {
    this.come = come;
  }

}
